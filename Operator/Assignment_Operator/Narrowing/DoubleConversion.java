class DoubleConversion{
     public static void main(String args[]){
	    
		double d=123.23;
		
	     float f=(float)d;
		 System.out.println(f);
		 
		 long l=(long)d;
		 System.out.println(l);
		 
		 int i=(int)d;
		 System.out.println(i);
		 
		 char ch=(char)d;
		 System.out.println(ch);
		 
		 short s=(short)d;
		 System.out.println(s);
		 
		 byte b=(byte)d;
		 System.out.println(b);
	 }
}