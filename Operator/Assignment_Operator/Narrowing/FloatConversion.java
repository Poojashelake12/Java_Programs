
class FloatConversion{
     public static void main(String[] args){
	     
		 float f=12.3f;
		 
		 int i=(int)f;
		 System.out.println(i);
		 
		 long l=(long)f;
		 System.out.println(l);
		 
		 char ch=(char)f;
		 System.out.println(ch);
		 
		 short s=(short)f;
		 System.out.println(s);
		 
		 byte b=(byte) f;
		 System.out.println(b);
		 
	 }
	 
	 
}